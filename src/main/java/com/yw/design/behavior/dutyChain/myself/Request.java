package com.yw.design.behavior.dutyChain.myself;

/**
 * 请假的例子，封装一个请求
 *
 * @author 18765
 */
public class Request {
    private String name;
    private int days;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDays() {
        return days;
    }

    public void setDays(int days) {
        this.days = days;
    }

}	
